// A function that adds two numbers
num addTwo(num n1, num n2) {
  return (n1 + n2);
}

//A function that subtracts two numbers
num subtractTwo(num n1, num n2) {
  return (n1 - n2);
}

//A function that multiplies two numbers
num multiplyTwo(num n1, num n2) {
  return (n1 * n2);
}

// A function that divides two numbers
num divideTwo(num n1, num n2) {
  return (n1 / n2);
}

// A function that returns the length of that string.
int stringLength(String word) {
  int len = word.length;
  return (len);
}

// A function that returns the first element of that list.
dynamic getFirstElement(List<dynamic> myList) {
  dynamic firstElement = myList.elementAt(0);
  return (firstElement);
}

